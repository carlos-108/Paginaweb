<h2>   Principales daños irreversibles a la salud</h2>
<p text-align="justify">Los trastornos en la salud causados por el consumo de cannabis inician con el desarrollo de una dependencia o adicción, caracterizada por la pérdida de control en el consumo y la aparición del síndrome de abstinencia.
        A corto plazo, las consecuencias que pueden ocasionar la muerte son brotes psicóticos, conductas suicidas, accidentes cerebrovasculares o paros cardiacos
        Uno de los mayores riesgos asociados a los efectos iniciales es la autolesión. La intensa percepción de alteración del entorno y la modificación del comportamiento son factores que provocan lesiones físicas entre las personas consumidoras. 
        A largo plazo, se presentan las siguientes consecuencias irreversibles: alteraciones de la conducta y trastornos de la salud mental, como psicosis, depresión, ansiedad y conducta suicida; afecciones cardiovasculares, Enfermedad Pulmonar Obstructiva Crónica (EPOC) y cánceres del aparato respiratorio.</p>
