<h2>Consecuencias no deseadas</h2>
<p text-align="justify">El consumo habitual de cannabis puede generar un estado de apatía y falta de motivación para realizar tareas. Además, las personas usuarias pueden experimentar un estado depresivo, que ocasiona falta de interés o satisfacción. </p>
        También pueden experimentarse episodios de ansiedad y paranoia al consumir cannabis, aun en bajas dosis. Asimismo, se pueden desencadenar desórdenes mentales latentes como psicosis o esquizofrenia en personas con predisposición genética.</p>
        Las alteraciones de procesos mentales como la disminución de la concentración y la falla de la memoria a corto plazo son consecuencias no deseadas que ocurren en cada consumo.</p>
        Uno de los efectos no deseados más comunes es la abrupta disminución de la presión arterial. Por ello, la persona siente un malestar generalizado que se agudiza con náuseas, mareos, vómito, piel pálida, ansiedad y una profunda desorientación. </p>
