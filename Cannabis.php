<h2>CANNABIS (MARIHUANA)</h2>
<br><p text-align="justify">¿Qué es?
        La cannabis es una planta que contiene sustancias llamadas cannabinoides con propiedades que modifican el comportamiento y alteran la percepción del entorno. Esta planta contiene al menos 750 compuestos químicos y unos 104 cannabinoides diferentes</p>
        Los principales cannabinoides presentes en esta planta son el 9-tetrahidrocannabinol (THC), el cannabidiol (CBD) y el cannabinol (CBN). El THC es el compuesto responsable de los efectos psicoactivos; el CBD es un compuesto no psicoactivo con algunas propiedades terapéuticas; suele emplearse como analgésico bajo supervisión médica.</p>
        A nivel mundial, la cannabis es la sustancia ilegal de mayor uso con 209 millones de personas consumidoras. En lo que respecta a los problemas de salud asociados a su consumo, aproximadamente, 13.1 millones de personas presentan trastornos.</p>
        El consumo de cannabis antes de los 15 años es un factor predictivo del abandono escolar temprano y de predisposición al consumo de otras drogas ilícitas.</p>
        Las personas que se inician en el consumo de cannabis tienen mayor probabilidad de usar otras sustancias. Existen dos factores que influyen en esto:<br>1. Los espacios para adquirir cannabis son los mismos donde se comercializan otras sustancias ilegales, lo que facilita el acceso a éstas.<br>2.Los efectos psicológicos del consumo de cannabis aumentan el interés de las personas jóvenes por otras sustancias.</p>
        De igual manera, existen factores psicoemocionales como baja autoestima, autocontrol insuficiente y escaso desarrollo de habilidades para la vida que inciden en la transición del consumo esporádico a una dependencia o adicción.</p>
