<h2>Efectos durante su consumo</h2>
<p text-align="justify"> Se emplea como tratamiento terapéutico del dolor crónico, pero de manera ilícita se toma para tener una sensación de relajación que suele durar entre tres y cinco horas. El abuso en el consumo de esta sustancia provoca los siguientes efectos:</p>
        <B>Físicos</B><br>• Estado de aletargamiento, adormecimiento.<br>• Sensación de sopor, ensoñación, sueño profundo. <br>• En el caso de una sobredosis se reducen las funciones vitales como la respiración y el pulso cardiaco, hasta llegar a la muerte.</p>
        <B>Psicológicos</B><br>• Sedación, placer.<br>• Pérdida del interés en las actividades cotidianas y en la convivencia interpersonal.<br>• Estado de aislamiento<br>• Pérdida de la conciencia.</p>
        <B>Efectos después de su consumo</B><br>Al detener su consumo, se produce un fuerte estado de abstinencia que se manifiesta con la dificultad para conciliar el sueño, irritabilidad, episodios de ansiedad por el deseo de volver a consumir y episodios de depresión. Los principales síntomas físicos son escalofríos, diarrea, náuseas, vómito, dolor muscular, dolor de huesos, calambres, sudoración excesiva, malestar estomacal, debilidad y fatiga.<br><br>Los síntomas de necesidad y falta de la droga (abstinencia) inician en cuanto se suspende el consumo y se pueden prolongar por varios días después de la última dosis. Se presentan con mayor fuerza de acuerdo con la frecuencia de consumo y a la cantidad de fentanilo acumulado en el organismo: a mayor dosis y mayor frecuencia, más fuerte es el deseo de seguir consumiendo. Aunque es posible que los síntomas físicos se reduzcan después de un par de semanas sin el consumo, los psicológicos aún se mantendrán.</p>
        <B>Principales daños irreversibles a la salud</B><br>• Descoordinación de las extremidades del cuerpo, como la falla al caminar o el movimiento de un solo lado del cuerpo.<br>• Alteraciones musculares, es decir, contracciones involuntarias o alteraciones del equilibrio y disminución de la fuerza.<br>• Problemas en la pronunciación y articulación e incoherencias al hablar.<br>• Alteraciones del sueño, pérdida de la memoria y del apetito.<br>• Trastornos en la salud mental: ansiedad y depresión.<br>• Problemas cardiacos.</p>
