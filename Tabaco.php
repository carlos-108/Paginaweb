<h2>Daños del humo de segunda mano</h2>
El humo ajeno, de segunda mano o secundario es producido por personas fumadoras y es respirado por quienes están alrededor. No existe una forma segura de exposición al humo ajeno. Incluso un breve tiempo puede causar daños, como irritación ocular, mareos o náuseas. Por otra parte, la exposición repetida o habitual al humo ajeno puede provocar enfermedades cardiovasculares, respiratorias graves y cáncer.</p>
        Las personas no fumadoras que conviven con personas consumidoras de productos de tabaco corren un mayor riesgo de padecer ese tipo de enfermedades y de morir prematuramente. Las y los lactantes y niños son especialmente susceptibles a padecer enfermedades del sistema respiratorio, del oído medio, hasta síndrome de muerte súbita.</p>
