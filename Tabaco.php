<h2>Efectos</h2>
<p text-align="justify">El consumo de nicotina puede producir sensación de relajación y de estimulación. Sin embargo, el síndrome de abstinencia y el deseo de consumir nuevamente se manifiestan al poco tiempo de iniciar su consumo. </p>
