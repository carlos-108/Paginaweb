<h2>Cómo se consume y en qué formas</h2>
<p text-align="justify"> El alcohol es la droga legal más consumida en el mundo. Se estima que existen 2,400 millones de personas que lo consumen. Las bebidas alcohólicas se ingieren por vía oral y se producen de dos maneras:</p>
      • Por fermentación: contienen entre 3 y 15 grados de alcohol. Algunos ejemplos son el vino, la cerveza y el pulque.<br>• Por destilación: se trata de líquidos conocidos como aguardientes o licores, los cuales contienen porcentajes de alcohol que varían entre los 20 y los 80 grados. Los más conocidos son ron, tequila, mezcal, vodka, whisky y brandy.</p>
