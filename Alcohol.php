<h2>ALCOHOL</h2>
<br><p text-align="justify">La sobredosis por el consumo de grandes cantidades de alcohol puede causar la muerte. Esta intoxicación se manifiesta con la disminución de las funciones vitales, principalmente, la respiración y la frecuencia cardiaca. Además, una persona que se encuentra altamente intoxicada por alcohol es propensa a sufrir accidentes o ejercer violencia grave.</p>
 ¿Qué es?
      El alcohol es una droga que tiene la capacidad de producir sedación. Los efectos de su consumo inician de forma leve, con desinhibición y euforia. El aumento en la cantidad ingerida provoca mareo, descoordinación, vómito, desequilibro, dificultad para hablar y confusión. Una ingesta desmedida puede inducir depresión grave del sistema nervioso e, incluso, la muerte. Además, beber alcohol causa adicción. </p>
      El consumo de bebidas alcohólicas en la adolescencia puede afectar el desarrollo del cerebro, la capacidad de construir relaciones saludables y altera el estilo de vida. Asimismo, este consumo puede disminuir el rendimiento escolar y, por la desinhibición que causa, puede acercar a las y los adolescentes a comportamientos sexuales de riesgo.</p>
      Por otra parte, la exposición a la publicidad de bebidas alcohólicas reduce la edad de inicio en su consumo y aumenta la probabilidad de tomarlas de forma regular. Ingerir bebidas alcohólicas antes de los 15 años incrementa los riesgos a la salud y la probabilidad de desarrollar dependencia del alcohol u otras drogas en los siguientes años.</p>
      Existen diversos aspectos en el consumo de alcohol que incrementan su impacto negativo en la salud, por ejemplo: el tiempo consumiendo, el patrón de consumo (días de consumo al mes y el número de copas por ocasión), beber hasta la embriaguez, la calidad de las bebidas ingeridas, así como su pureza (si el alcohol es adulterado con sustancias nocivas, como el metanol, se vuelve mucho más peligroso). </p>
      
