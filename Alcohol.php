<h2>Consecuencias irreversibles para la salud</h2>
<p text-align="justify">El alcohol es un factor de riesgo asociado a más de 200 trastornos de salud física y mental, como enfermedades cardiovasculares, problemas del hígado, algunos tipos de cáncer y accidentes con desenlaces fatales (de tránsito o riñas).</p>
      Por su parte, el inicio en el consumo de alcohol durante la adolescencia puede estar asociado a trastornos de salud mental, como depresión y ansiedad. De igual forma, el consumo de bebidas alcohólicas por personas jóvenes está vinculado con intentos de suicidio y muerte por esta causa.</p>
      El consumo de bebidas alcohólicas puede causar dolores de cabeza y migrañas durante y después de su consumo. Durante la resaca se manifiesta esta consecuencia no deseada. Asimismo, trastornos del sueño pueden aparecer desde la primera vez que se toma y durante el consumo frecuente. </p>
      Otras consecuencias son las alteraciones en la atención y concentración para realizar las actividades diarias, laborales o escolares. Así como la dificultad para controlar impulsos, alteraciones del juicio o dificultad en la toma de decisiones, y déficit en el aprendizaje. </p>
      Una de las consecuencias negativas más características del consumo excesivo de bebidas alcohólicas es la pérdida de la memoria de corto plazo, es decir, la falta de recuerdos de las situaciones ocurridas bajo los efectos del alcohol.</p>
