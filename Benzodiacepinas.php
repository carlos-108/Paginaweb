<h2>Consecuencias irreversibles para la salud</h2>
<p text-align="justify">El uso no supervisado de las benzodiacepinas puede generar dependencia. En grandes cantidades o por periodos prolongados, el abuso de esta sustancia puede causar daños a las funciones motoras, hipotensión, depresión respiratoria, coma y la muerte.
        Los numerosos casos de muertes asociadas al consumo de benzodiacepinas ocurren fuera de un tratamiento médico y porque, normalmente, se les mezcla con otras sustancias como alcohol, estimulantes u opioides.
        El consumo de benzodiacepinas tiene efectos no deseados como arritmia del corazón, hiperventilación y convulsiones. Los síntomas del síndrome de abstinencia ocasionados por detener abruptamente su consumo pueden ser ataques de ansiedad y depresión, dolores de cuerpo, mareos y escalofríos.
       El consumo de estas sustancias de uso médico también tienen un impacto a largo plazo, principalmente en la modificación de la conducta de las personas. Los daños que las benzodiacepinas como el clonazepam o diazepam pueden ocasionar son pérdida de la visión, disminución en la capacidad y agilidad del pensamiento, alteración en la memoria y en la concentración, así como dificultad para hablar. Estas consecuencias mencionadas pueden producirse a cualquier edad y pueden llegar a ser irreversibles.
        La ingesta de benzodiacepinas puede acarrear problemas educativos, laborales, familiares y con amistades, autoexclusión social e intentos de suicidio. </p>
