<h2>Benzodiacepinas</h2>
<br><p text-align="justify">¿Qué son?
La combinación de benzodiacepinas con otras sustancias que deprimen el Sistema Nervioso Central, como alcohol, cannabis, solventes, heroína y fentanilo puede producir un efecto de sedación muy potente, que incrementa el riesgo de sufrir un paro respiratorio y la muerte. Estas mezclas han sido asociadas con diferentes tipos de delitos, principalmente, el abuso sexual.
Las benzodiacepinas son uno de los grupos de medicamentos más usados a nivel mundial por sus efectos depresores y moduladores del Sistema Nervioso Central. Bajo prescripción médica son altamente utilizados en el tratamiento de la ansiedad y para el trastorno del sueño. También son recetados como anticonvulsivos, ansiolíticos y relajantes musculares.
       Sin embargo, el tratamiento médico con benzodiacepinas suele administrarse por un corto periodo y en dosis bajas debido a su alto potencial adictivo. Con el uso prolongado se puede desarrollar tolerancia, síndrome de abstinencia y otras consecuencias no deseadas. 
       Por lo tanto, su venta es únicamente bajo prescripción médica con receta controlada y a personas mayores de edad. 
        Las benzodiacepinas más conocidas son el clonazepam, diazepam y el alprazolam. Las unidades de emergencia hospitalaria han reportado intoxicaciones por su uso indebido en personas menores de edad y en adultas.
