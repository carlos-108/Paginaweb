<section id="contact" class="content">
<h2>Principales daños irreversibles a la salud</h2>
<p text-align="justify">        
El uso de vapeadores, con y sin nicotina, provoca lesiones en el tejido del sistema respiratorio, principalmente en las vías respiratorias altas. Asimismo, disminuye la cantidad de oxígeno en la respiración de personas que eran no fumadoras y vapearon.
        El vapeo con consumo de nicotina está asociado a dolores abdominales, diarrea e infecciones en vías respiratorias altas. La exposición continua al sobrecalentamiento de los productos ocasiona heridas en la piel como quemaduras, dermatitis y lesiones en boca y garganta. Se ha documentado que el uso de vapeadores está relacionado a la presencia de características del síndrome metabólico, disminución de insulina en la sangre y fallas en el corazón.
        El inicio de consumo de tabaco o nicotina en la adolescencia suele provocar dependencia de esta sustancia en años posteriores. Las y los adolescentes y jóvenes que inician el uso de vapeadores tienen una probabilidad entre dos y tres veces mayor de consumir regularmente cigarrillos convencionales que las personas que no lo hacen.
        Entre adolescentes, principalmente estudiantes de secundaria, el uso de vapeadores alguna vez en la vida se correlacionó con una mayor probabilidad de consumo excesivo de alcohol. La edad es un factor de riesgo, ya que a menor edad de inicio en el vapeo, la probabilidad de consumir alcohol incrementa.
        La asociación también se ha encontrado a la inversa, es decir, las y los adolescentes consumidores de alcohol son más susceptibles a convertirse en fumadores o vapeadores.</p>
