<h2>Modo de consumo</h2>
<p>        Existe una amplia variedad de vapeadores. Los SEAN se comercializan en forma de cigarrillos, cigarros, puritos, pipas o narguiles; otros tienen la forma de objetos del día a día como bolígrafos, lápices, USB y dispositivos cilíndricos o prismáticos.
        La cantidad de nicotina que consume una persona a través de los SEAN depende de factores como la duración de la calada, la profundidad de la inhalación y la frecuencia de uso.</p>
