<h2>¿Qué son?</h2>
<p text-align="justify">Los vapeadores (cigarrillos electrónicos o e-cigs) son dispositivos electrónicos que administran nicotina, sustancia psicoactiva estimulante y con un alto potencial adictivo. Estos dispositivos incluyen un cartucho desechable o recargable que es calentado para permitir la inhalación de un líquido compuesto por nicotina, formaldehído, propilenglicol, saborizantes y aromatizantes. 
        Existen dos tipos de dispositivos: los Sistemas Electrónicos de Administración de Nicotina (SEAN) y los que no contienen nicotina. Al usar estos dispositivos es difícil definir la dosis de nicotina que se consume en cada inhalación porque depende de la forma en que cada persona utilice el vapeador, lo cual vuelve esta práctica muy riesgosa.
        Se tiene la creencia de que el uso de vapeadores no conlleva un grave riesgo para la salud porque el porcentaje de nicotina que introducen al cuerpo es menor. Sin embargo, las personas usuarias de vapeadores que padecen tabaquismo presentan un fenómeno “compensatorio”, es decir, aumentan el número de inhalaciones para recibir la dosis de nicotina a la que están acostumbradas.
        La industria tabacalera desarrolló estos productos como auxiliares para dejar de fumar. Actualmente, no hay suficiente evidencia documentada que demuestre la efectividad de los vapeadores. Por el contrario, se ha reportado que estos productos generan dependencia rápidamente, por su sabor agradable y la baja percepción de riesgo. Además, estas empresas utilizan viejas prácticas de mercadotecnia dirigidas a adolescentes y personas jóvenes para engancharlas, sin reparar en las consecuencias. </p>
</section>


