<h2>Efectos durante su consumo</h2>
<p text-align="justify">        El uso de vapeadores con o sin nicotina genera una percepción de relajación. Sin embargo, algunos daños relacionados son vómitos, mareos, irritación ocular, en boca y en nariz.</p>
